    private void bAddCorsoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddCorsoActionPerformed
        // TODO add your handling code here:
        String nc = tnomeCorso.getText();
        String ed = tedizione.getText();
        String sno = tnumOre.getText();
        int no;
        try {
            no = Integer.parseInt(sno);
        } catch (Exception e) {
            no = 0;
        }
        // tutto pronto per nuovo corso da aggiungere
        // creo nuovo corso
        Corso newcorso = new Corso(nc);
        newcorso.setEdizione(ed);
        newcorso.setNumOre(no);
        //aggiungo il corso all'elenco
        elencoCorsi.add(newcorso);
        //aggiorno il tabellone tpelencocorsi con la nuova versione
        tpelencocorsi.setText(refreshElencoCorsi());
        tnomeCorso.setText("");
        tedizione.setText("");
        tnumOre.setText("0");
        
    }//GEN-LAST:event_bAddCorsoActionPerformed

